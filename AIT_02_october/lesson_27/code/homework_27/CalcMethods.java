package homework_27;

public class CalcMethods {
    public int sum(int a, int b) {
        return a + b;
    }
    public int dif (int a, int b) {
        return a - b;
    }
    public int div (int a, int b){
        return a / b;
    }
    public int multiplication (int a, int b){
        return a * b;
    }

}
